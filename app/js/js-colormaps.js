// Functions
function enforceBounds(x) {
  if (x < 0) {
    return 0;
  } else if (x > 1) {
    return 1;
  } else {
    return x;
  };
};

function interpolateLinearly(x, values) {

  // Split values into four lists
  var x_values = [];
  var r_values = [];
  var g_values = [];
  var b_values = [];
  for (i in values) {
    x_values.push(values[i][0]);
    r_values.push(values[i][1][0]);
    g_values.push(values[i][1][1]);
    b_values.push(values[i][1][2]);
  }

  var i = 1;
  while (x_values[i] < x) {
    i = i + 1;
  }
  i = i - 1;

  var width = Math.abs(x_values[i] - x_values[i + 1]);
  var scaling_factor = (x - x_values[i]) / width;

  // Get the new color values though interpolation
  var r = r_values[i] + scaling_factor * (r_values[i + 1] - r_values[i])
  var g = g_values[i] + scaling_factor * (g_values[i + 1] - g_values[i])
  var b = b_values[i] + scaling_factor * (b_values[i + 1] - b_values[i])

  return [enforceBounds(r), enforceBounds(g), enforceBounds(b)];

};

function drawColormap(object_id, bar_width, max_value, theme) {

  var points = [];
  var c = document.getElementById(String(object_id));
  var ctx = c.getContext("2d");

  for (i = 0; i <= bar_width; i++) {
    var color = interpolateLinearly(i / bar_width, window[theme]);
    r = Math.round(255 * color[0]);
    g = Math.round(255 * color[1]);
    b = Math.round(255 * color[2]);
    ctx.fillStyle = 'rgb(' + r + ', ' + g + ', ' + b + ')';
    ctx.fillRect(i, 0, 1, 50);

    if (theme === "seismic") {
      display_value = (-max_value + ((2 * max_value) * (i / bar_width)));
    } else {
      display_value = (i / bar_width) * max_value;
    }
    points.push({
      x: ctx.canvas.offsetLeft + i,
      y: ctx.canvas.offsetTop,
      r: 4,
      rXr: 16,
      value: display_value
    });
  };
  return [points, ctx.canvas.offsetLeft - 1];
};

function handleMouseMove(e, points, theme) {

  var labels = points[0];
  var offsetX = points[1];
  mouseX = e.clientX - offsetX;

  if (theme === "seismic") {
    display_value = Math.round(labels[mouseX].value);
  } else {
    display_value = (labels[mouseX].value).toFixed(1);;
  };

  var x = (e.clientX + 15) + 'px',
    y = (e.clientY + 10) + 'px';

  return [display_value, x, y];

};

